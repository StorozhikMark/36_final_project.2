# Вариант 1 — Telegram бот "Тамагочи"
# Описание проекта:
# Создать чат-бота для Telegram, который симулирует виртуального питомца. У пользователя есть команды для ухода за питомцем, бот ведет учёт состояния питомца: голода, счастья, здоровья. Состояние питомца меняется со временем или в зависимости от действий пользователя.
# Основной функционал:
# - Команды: /start, /status, /feed, /play, /heal
# - Состояния: голод, счастье, здоровье (например, от 0 до 100)
# - Хранение текущего состояния в текстовом файле
# - Обновление состояния после каждой команды
# - Автоматическое снижение показателей со временем (можно раз в час или по каждому вызову команды)